				<h1>Constitution</h1>
				<h4>NAME</h4>
				<p>1.1 : There shall be a society called The UCD Internet Access Society, also known as netsoc.</p> 
				<h4>OBJECTS</h4>

<p>2.1 : The objects of the society shall be: </p>
<p>* 2.1.1 : To develop and encourage the interest of the UCD community in the Internet; </p>
<p>* 2.1.2 : To run for the benefit of the members equipment capable of carrying out Internet-related 
				  functions, and supplying services in return for membership fees; </p>
<p>* 2.1.3 : To educate and inform on Internet-related topics wherever possible. </p>
<p>2.2 : The funds of the Society shall be applied for the purpose of furthering these objects. </p> 

<h4>MEMBERSHIP</h4>

<p>3.1 : Membership of the Society shall be open to all students, staff and post graduates the college.</p>

<p>3.2 : Membership shall be obtained by either paying the appropriate membership fee and signing the 
				roll of members, or by being a member of the Executive or System Administration Committee for that year. </p>

<p>3.3 : The Membership and committees of the society, shall be bound by the rules and membership rights 
				as documented and determined by the Executive Committee.</p>

<h4>EXECUTIVE COMMITTEE</h4>

<p>4.1 : There shall be a committee of the Society (hereinafter known as the Executive Committee), which, 
				subject to this constitution and to the decisions made in General Meetings of the Society, 
				shall be the governing body of the Society. </p>

<p>4.2 : The Executive Committee shall consist of: <br/>
				* The Auditor; <br/>
				* The Secretary; <br/>
				* The Treasurer; <br/>
				* The Head System Administrator; <br/> 
				* 3 Ordinary Committee Members (OCM's).</p>

<p>4.3 : All the members of the Executive Committee (except the Head System Administrator) shall be elected 
				at the Annual General Meeting of the Society, held yearly. Their term of office shall be from the 
				end of the academic term in which they are elected, until the same time the following year. </p>
   
<p>5.1 : Meetings of the Executive Committee shall be convened by the Auditor or by requisition of any two 
				members of the Executive Committee. Notice of any such meeting shall be given, where possible, to 
				all members of the Executive Committee at least 24 hours before the time of such a meeting. This 
				requirement for notice applies to both Auditorially-convened meetings as well as ones convened by 
				members of the Executive Committee. </p>

<p>6.1 : The quorum for any meeting of the Executive Committee shall be 4 voting members. </p>
OFFICERS OF THE EXECUTIVE COMMITTEE

<p>7.1 : There shall be an Auditor, who shall be the chief officer of the Society. The Auditor shall be entitled 
to preside at all General Meetings of the Society and at all meetings of the Executive Committee. The 
Auditor may decide to appoint another Executive Committee member to chair a meeting in his/her absence. 
At meetings of the Executive Committee, the Auditor (or his/her appointee) shall have both a deliberative 
and a casting vote. At General Meetings, the Auditor shall have only a casting vote. </p>


<p>8.1 : There shall be a secretary of the Society. The duties of the secretary shall include (but are not limited to): </p>
<p>* 8.1.1 : Recording the minutes of all meetings of the Executive Committee and of all General Meetings;</p>
<p>* 8.1.2 : Undertaking such correspondence of the Society as is needed, directed by the Auditor and/or Executive</p> 
<h4>Committee;</h4>
<p>* 8.1.3 : Keeping the records, other than the financial or equipment related records, of the Society;</p>
<p>* 8.1.4 : Giving due notice of all meetings of the Society and of its Executive Committee;</p>
<p>* 8.1.5 : Keeping a roll of members of the Society.</p>

<p>9.1 : There shall be a treasurer of the Society. The duties of the Treasurer shall include (but are not limited to):</p> 
<p>* 9.1.1 : Managing such financial affairs of the Society as are necessary; </p>
<p>* 9.1.2 : In association with the Senior Treasurer, maintaining and operating a bank account in the name of the 
Society; </p>
<p>* 9.1.3 : Reporting to each meeting of the Executive Committee on the state of the Society's finances;</p> 
<p>* 9.1.4 : In co-operation with the Senior Treasurer, preparing an annual report on the financial transactions 
of the Society;</p> 
<p>* 9.1.5 : Presenting an Annual Financial Report to the Annual General Meeting of the Society; </p>

<h4>SENIOR TREASURER</h4>

<p>10.1 : There shall be a Senior Treasurer of the Society; who shall be chosen by the Executive Committee from amongst
the full-time members of the staff of the college, and shall have such responsibilities as are laid down by 
the college, and by this self-same constitution. </p>

<h4>SYSTEM MANAGEMENT</h4>

<p>11.1 : There shall be a Head System Administrator, who shall be a member of both the Executive Committee and the 
System Administration Committee. The duties of this officer shall include (but are not limited to): </p>
<p>* 11.1.1 : Fully co-operating and liasing with the relevant authorities whose interests are convergent with that 
  of the Society within the college, and reporting to the Executive Committee on the status of this 
  co-operation; </p>
<p>* 11.1.2 : Maintaining and defending the good name of the Society as far as possible when disciplinary issues 
  arise in proper collusion with these authorities; </p>
<p>* 11.1.3 : Maintaining and operating such equipment as the Society owns for the benefit and use of its members 
  as outlined in Section 2 above, and reporting to the Executive Committee on the status of such equipment; </p>
<p>* 11.1.4 : Keeping records of whatever is done concerning the equipment that the Society possesses; </p> 
<p>* 11.1.5 : Maintaining the security and preserving the relevance of the Memberships trust; </p>

<p>12.1 : The Head System Administrator shall appoint a committee (hereinafter known as the System Administration 
  Committee) to aid him in the execution of his duties as listed above. </p>

<p>13.1 : The system administration committee shall consist of:</p>
<p>* The Head system administrator </p>
<p>* 3 ordinary system administrators </p>
 
<p>14.1 : The 3 ordinary system administrators shall be appointed by the head system administrator.</p>
 
<p>15.1 : Provision shall also be made for the positions of consulting system administrators, who will not have a 
  vote in system administration decisions but may be called on to advise the head system administrator and 
  the ordinary system administrators in said decisions.</p>
 
<p>16.1 : The number of consulting system administrators shall be at the discretion of the head system administrator.</p>

<h4>GENERAL MEETINGS</h4>

<p>17.1 : There shall be at least one General Meeting of the Society in each term of every academic year. Subject 
  to this constitution, the agenda for each such meeting shall be arranged by the committee (subject to 
  the powers of the ordinary members of the society as laid down herein).</p>

<p>17.2 : The Annual General Meeting of the society shall be held in the Trinity Term. At this Meeting, the business 
  conducted shall include the election of officers and the delivery of annual reports by the outgoing officers. </p>

<p>17.3 : An Extraordinary General Meeting must be convened if requested in writing by at least 20% of or 50 members 
  of the Society. </p>

<p>17.4 : Notice of any General Meeting shall be given to all members at least 5 working days before such meeting 
  by means of notices displayed in suitably public areas, and by Internet-related methods if possible. </p>

<p>17.5 : Motions for discussion at a General Meeting may be proposed by any member of the Society. Such motions must 
  be submitted in writing, or by electronic means to the Auditor at least 24 hours before the start of the 
  meeting. </p>

<p>17.6 : The quorum for any General Meeting shall be 20 or 20% of the membership, whichever is the lesser. </p>



<h4>ELECTIONS</h4>

<p>18.1 : Elections to all offices of the Society, except that of the Head System Administrator, shall take place 
  at the Annual General Meeting of the Society. </p>

<p>18.2 : Prior to the Annual General Meeting, the Executive Committee shall appoint a Returning Officer who 
  shall be responsible for the conduct of the elections. The Executive Committee shall give notice of 
  such an appointment.</p>

<p>18.3 : The Returning Officer shall not be a candidate for any other office within the Society, nor for 
  membership of the Executive Committee. </p>

<p>18.4 : Nominations for the office of Auditor shall be signed by any four members of the Society and shall 
  be given to the Returning Officer at least 3 days prior to the date of the election. </p>

<p>18.5 : To be eligible for nomination a candidate shall have paid the appropriate subscription prior to the 
  end of the Hillary Term immediately preceding, or have been a member of the Executive Committee prior 
  to the end of the self same term.</p>

<p>18.5 : To be eligible for nomination to the position of Auditor, a candidate shall have been been a member 
  of the Executive Committee or System Administration Committee for at least two terms preceding the 
  term in which the Annual General meeting is held.  Only if there are no nominees meeting these criteria 
  shall the position be opened to the rest of the membership.</p>

<p>18.6 : Nominations for the other offices of the Executive Committee, excluding the Head System Administrator, 
  shall be given to the Returning Officer up to 24 hours before the start of the Annual General Meeting. </p>

<p>18.7 : To be eligible for nomination, a candidate shall have paid the appropriate subscription prior to the 
  end of the Hillary term immediately preceding, or have been a member of the Executive Committee prior 
  to the end of the self-same term. </p>

<p>18.8 : The Returning Officer, and the Executive Committee, shall arrange the details of the election. </p>

<p>18.9 : Any vacancy arising in any office or among the members of the Executive Committee, for whatever reason, 
  shall be filled at the Executive Committees discretion. </p>


<p>19.1 : The incoming Head System Administrator shall be elected at a joint meeting of the outgoing Executive 
  Committee and the outgoing System Administration Committee. At this meeting, the Auditor, Secretary, 
  Treasurer, the 3 Ordinary System Administrators and the 3 Ordinary Committee Members shall each have 
  one vote, whereas the outgoing Head System Administrator shall have two votes. The outgoing Auditor 
  shall have a casting vote. </p>

<h4>DISMISSAL FROM OFFICE</h4>

<p>20.1 : No officer or member of the Executive Committee, except the Head System Administrator, shall be dismissed 
  from office unless the following procedures are carried out: </p>

<p>20.2 : Notice of a resolution to seek such dismissal, signed by at least 20 members of the Society, shall be 
  given to the Auditor of the Society at least 5 working days prior to the General Meeting of the Society 
  at which such resolution will be debated. </p>

<p>20.3 : The Auditor shall give notice of such resolution to the members at least 5 working days prior to the 
  General Meeting. </p>

<p>20.4 : The resolution shall have been passed by a 2/3rds majority at the General Meeting. </p>


<p>21.1 : The Head System Administrator shall be dismissed from office only by a two thirds majority decision of 
  a joint meeting of the Executive Committee and the System Administration Committee, excluding the Head 
  System Administrator. </p>

<p>22.1 : The Head System Administrator may dismiss from office an Ordinary System Administrator. </p>

<h4>GENERAL</h4>

