  To this end, the Society shall comply with all reasonable requests from the Academic Council of the college 
  or its officers, or other disciplinary bodies that have an interest in the workings of the Society. </p>

<p>24.2 : This constitution shall come into effect when it has been approved by 2/3rds of the members of the Society 
  present at a meeting specially convened to consider the matter. </p>

<p>24.3 : Notice of a proposal to seek an amendment to this constitution shall be given to all members at least 5 
  working days prior to the General Meeting at which such proposal will be considered. Such a proposal must 
  receive the approval of 2/3rd of the members present at such a meeting. </p>

<p>24.4 : Notice of any amendment which has been duly approved shall be forwarded to the Registrar of the College, 
  and Student Consultative Forum, by the outgoing secretary, within 1 week of the end of the Trinity Term, 
  that the AGM was held in.</p>
